class Pattern:

    def __init__(self, name, pat):
        self.name = name
        self.pat = pat

    def get(self, c, r):
        return ((0 <= r < len(self.pat))
                and (0 <= c < len(self.pat[r]))
                and (self.pat[r][c] == '@'))

def p(name, data):
    return Pattern(name, data)

